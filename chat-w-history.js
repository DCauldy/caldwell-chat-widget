    // --- SESSION PERSISTENCE ---
    let currentSessionId = '';

    function getSessionId() {
        let sessionId = localStorage.getItem('n8nChatSessionId');
        if (!sessionId) {
            sessionId = crypto.randomUUID();
            localStorage.setItem('n8nChatSessionId', sessionId);
        }
        return sessionId;
    }

